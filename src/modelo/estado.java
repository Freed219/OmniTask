package modelo;

/**
 *
 * @author admin
 */
public class estado {
    private int id;
    private String name;
    private String abreviatura;

    public estado() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAbreviatura() {
        return abreviatura;
    }

    public void setAbreviatura(String abreviatura) {
        this.abreviatura = abreviatura;
    }

    @Override
    public String toString() {
        return "estado{" + "id=" + id + ", name=" + name + ", abreviatura=" + abreviatura + '}';
    }
}
